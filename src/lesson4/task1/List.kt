/**
 * Сложная (5 баллов)
 *
 * Перевести натуральное число n > 0 в римскую систему.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */
fun roman(n: Int): String {
    val rules = mutableMapOf(
        "M" to 1000,
        "CM" to 900,
        "D" to 500,
        "CD" to 400,
        "C" to 100,
        "XC" to 90,
        "L" to 50,
        "XL" to 40,
        "X" to 10,
        "IX" to 9,
        "V" to 5,
        "IV" to 4,
        "I" to 1
    )
    var res = ""
    var num = n
    val roman = (rules.keys).toTypedArray()
    for (i in 0 until rules.size) {
        val const = rules[roman[i]]!!.toInt()
        while (num >= const) {
            num -= const
            res += roman[i]
        }
    }
    return res
}

/**
 * Очень сложная (7 баллов)
 *
 * Записать заданное натуральное число 1..999999 прописью по-русски.
 * Например, 375 = "триста семьдесят пять",
 * 23964 = "двадцать три тысячи девятьсот шестьдесят четыре"
 */
fun russian(n: Int): String {
    val hundredList =
        listOf("", "сто", "двести", "триста", "четыреста", "пятьсот", "шестьсот", "семьсот", "восемьсот", "девятьсот")
    val tenList = listOf(
        "",
        "десять",
        "двадцать",
        "тридцать",
        "сорок",
        "пятьдесят",
        "шестьдесят",
        "семьдесят",
        "восемьдесят",
        "девяносто"
    )
    val tenSubList = listOf(
        "десять",
        "одиннадцать",
        "двенадцать",
        "тринадцать",
        "четырнадцать",
        "пятнадцать",
        "шестнадцать",
        "семнадцать",
        "восемнадцать",
        "девятнадцать"
    )
    val thousand = n / 1000
    val hundred = n / 100 % 10
    val ten = n / 10 % 10
    val unit = n % 10
    val res = mutableListOf<String>()
    val unitList = if (thousand % 10 != 0) listOf(
        "",
        "одна",
        "две",
        "три",
        "четыре",
        "пять",
        "шесть",
        "семь",
        "восемь",
        "девять"
    ) else listOf("", "один", "два", "три", "четыре", "пять", "шесть", "семь", "восемь", "девять")
    val unitList2 = listOf("", "один", "два", "три", "четыре", "пять", "шесть", "семь", "восемь", "девять")
    when (digitNumber(thousand)) {
        3 -> {
            res.add(hundredList[thousand / 100])
            when (thousand / 10 % 10) {
                1 -> res.add(tenSubList[thousand % 10])
                else -> {
                    res.add(tenList[thousand / 10 % 10])
                    if (thousand % 10 != 0) res.add(unitList[thousand % 10])
                }
            }
        }
        2 -> {
            if (thousand / 10 == 1) res.add(tenSubList[thousand % 10])
            else {
                res.add(tenList[thousand / 10])
                res.add(unitList[thousand % 10])
            }
        }
        1 -> res.add(unitList[thousand])
        else -> res.add("")
    }
    if (thousand != 0 && n > 0) {
        if (thousand / 10 % 10 == 1) res.add("тысяч")
        else if (thousand % 10 == 1) res.add("тысяча")
        else if (thousand % 10 in 2..4) res.add("тысячи")
        else res.add("тысяч")
    }
    if (hundred != 0) res.add(hundredList[hundred])
    when {
        ten == 1 -> res.add(tenSubList[unit])
        ten != 0 -> res.add(tenList[ten])
    }
    if (ten != 1 && unit != 0) res.add(unitList2[unit])
    return (res.filter { it != "" }.joinToString(" "))
}